package Begin;

public class ResultDataType {
    public static void main(String[] args) {
        int i = 10;
        int b = i++;
        int c = ++i;
        System.out.println(b);
        System.out.println(c);
        int d = i;
        System.out.println(d);
        char a = 'b';
        System.out.println(++a);
    }
}
